function sum(x, y) {
    return x + y
}

function minus(x, y) {
    return x - y
}

function divide(x, y) {
    return x / y
}

function times(x, y) {
    return x * y
}
 
let a = 5
let b = 6

console.log(`A soma dos numeros ${a} e ${b} da: ${sum(a, b)}`)
console.log(`A subtração dos numeros ${a} e ${b} da: ${minus(a, b)}`)
console.log(`A divisão dos numeros ${a} e ${b} da: ${divide(a, b)}`)
console.log(`A multiplicação dos numeros ${a} e ${b} da: ${times(a, b)}`)